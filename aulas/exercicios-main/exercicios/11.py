# Faça um Programa que peça 2 números inteiros e um número real. Calcule e mostre

n1 = int(input('Digite 1º número inteiro: '))
n2 = int(input('Digite 2º número inteiro: '))
n3 = float(input('Digite um número real: '))

produto = (n1*n1)*(n2/2)
soma = (n1*3)+n3
cubo = n3**3

print('O Resultado do Produto => ',produto)
print('O Resultado da Soma => ',soma)
print('O Resultado do Cubo => ',cubo)

